package com.epam.geometry.logic;

public class CubeData {
    private final double area;
    private final double volume;

    public CubeData(double area, double volume) {
        this.area = area;
        this.volume = volume;
    }

    public double getArea() {
        return area;
    }

    public double getVolume() {
        return volume;
    }

}
